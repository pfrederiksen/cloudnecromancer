// Export writes the snapshot as a Pulumi TypeScript program.
func (e *PulumiExporter) Export(snapshot *engine.Snapshot, w io.Writer) error {
	// Collect required modules
	modules := make(map[string]bool)
	for typeKey := range snapshot.Resources {
		if r, ok := pulumiMapping[typeKey]; ok {
			modules[r.Module] = true
		}
	}

	fmt.Fprintf(w, "// RECONSTRUCTED -- verify before deploying\n")
	fmt.Fprintf(w, "// Generated by CloudNecromancer at %s\n\n", snapshot.Timestamp.Format(time.RFC3339))
	fmt.Fprintf(w, "import * as aws from \"@pulumi/aws\";\n\n")

	for typeKey, resources := range snapshot.Resources {
		pr, known := pulumiMapping[typeKey]
		if !known {
			fmt.Fprintf(w, "// Unsupported: %s (skipped)\n", sanitizeComment(typeKey))
			continue
		}

		for _, res := range resources {
			varName := pulumiVarName(res.ResourceID)
			fmt.Fprintf(w, "const %s = new aws.%s.%s(%q, {\n", varName, pr.Module, pr.Class, res.ResourceID)
			for _, line := range pr.PropLines(res) {
				fmt.Fprintf(w, "%s\n", line)
			}
			fmt.Fprintf(w, "});\n\n")
		}
	}

	return nil
}

func pulumiVarName(resourceID string) string {
	name := strings.Map(func(r rune) rune {
		if (r >= 'a' && r <= 'z') || (r >= 'A' && r <= 'Z') || (r >= '0' && r <= '9') || r == '_' {
			return r
		}
		return '_'
	}, resourceID)
	// Ensure starts with letter
	if len(name) > 0 && name[0] >= '0' && name[0] <= '9' {
		name = "_" + name
	}
	return name
}
