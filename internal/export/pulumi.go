// PulumiExporter writes a Snapshot as a Pulumi (TypeScript) program.
type PulumiExporter struct{}

type pulumiResource struct {
	Package   string // e.g. "@pulumi/aws"
	Module    string // e.g. "ec2"
	Class     string // e.g. "Instance"
	PropLines func(res engine.Resource) []string
}

